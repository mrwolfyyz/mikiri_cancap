async function getMarkdown(jobId) {
    try {
        const token = await getAuthToken();
        const response = await fetch(`${API_URL}/get_markdown/${jobId}`, {
            headers: {
                "Authorization": `Bearer ${token}`
            }
        });
        if (!response.ok) {
            if (response.status === 401) {
                // Try refresh
                const newToken = await getAuthToken();
                const retryResponse = await fetch(`${API_URL}/get_markdown/${jobId}`, {
                    headers: {
                        "Authorization": `Bearer ${newToken}`
                    }
                });
                if (!retryResponse.ok) {
                    throw new Error("Authentication failed. Please refresh the page.");
                }
                return await retryResponse.json();
            }
            const errorData = await response.json().catch(() => ({}));
            throw new Error(errorData.error || `HTTP ${response.status}`);
        }
        return await response.json();
    } catch (error) {
        console.error("Error fetching markdown:", error);
        throw error;
    }
}

async function sendChatMessage(payload) {
    try {
        const token = await getAuthToken();

        // Add job_id to payload for ownership verification
        if (jobId) {
            payload.job_id = jobId;
        }

        const chatEndpoint = PlatformConfig.get("chatEndpoint");

        const response = await fetch(`${API_URL}${chatEndpoint}`, {
            method: "POST",
            headers: {
                "Content-Type": "application/json",
                "Authorization": `Bearer ${token}`
            },
            body: JSON.stringify(payload),
        });

        if (!response.ok) {
            if (response.status === 401) {
                // Try refresh
                const newToken = await getAuthToken();
                const retryResponse = await fetch(`${API_URL}${chatEndpoint}`, {
                    method: "POST",
                    headers: {
                        "Content-Type": "application/json",
                        "Authorization": `Bearer ${newToken}`
                    },
                    body: JSON.stringify(payload),
                });
                if (!retryResponse.ok) {
                    throw new Error("Authentication failed. Please refresh the page.");
                }
                return await retryResponse.json();
            }
            const errorData = await response.json().catch(() => ({}));
            throw new Error(errorData.error || `HTTP ${response.status}`);
        }

        return await response.json();
    } catch (error) {
        console.error("Error sending chat message:", error);
        throw error;
    }
}

// ===========================
// Markdown Rendering
// ===========================
function renderMarkdown(text) {
    if (!text) return '';

    // Escape HTML first to prevent XSS
    let html = text
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;');

    // Headers (### and ##)
    html = html.replace(/^### (.+)$/gm, '<h4>$1</h4>');
    html = html.replace(/^## (.+)$/gm, '<h3>$1</h3>');

    // Bold (**text**)
    html = html.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');

    // Italic (*text*)
    html = html.replace(/\*([^*]+)\*/g, '<em>$1</em>');

    // Inline code (`code`)
    html = html.replace(/`([^`]+)`/g, '<code>$1</code>');

    // Links [text](url) — only http(s); escape href for attribute safety
    html = html.replace(/\[([^\]]+)\]\(([^)]+)\)/g, (match, linkText, url) => {
        const safe = sanitizeMarkdownLinkUrl(url);
        return `<a href="${escapeHtmlAttr(safe)}" target="_blank" rel="noopener noreferrer">${linkText}</a>`;
    });

    // Unordered lists (- item)
    html = html.replace(/^- (.+)$/gm, '<li>$1</li>');
    html = html.replace(/(<li>.*<\/li>)\n(?=<li>)/g, '$1');
    html = html.replace(/(<li>[\s\S]*?<\/li>)(?!\n<li>)/g, '<ul>$1</ul>');

    // Numbered lists (1. item)
    html = html.replace(/^\d+\. (.+)$/gm, '<li>$1</li>');

    // Paragraphs (double newlines)
    html = html.replace(/\n\n+/g, '</p><p>');

    // Single newlines to <br> (but not inside lists)
    html = html.replace(/([^>])\n([^<])/g, '$1<br>$2');

    // Wrap in paragraph if not starting with block element
    if (!html.match(/^<(h[34]|ul|ol|p)/)) {
        html = '<p>' + html + '</p>';
    }

    // Clean up empty paragraphs
    html = html.replace(/<p><\/p>/g, '');
    html = html.replace(/<p>\s*<\/p>/g, '');

    return html;
}

