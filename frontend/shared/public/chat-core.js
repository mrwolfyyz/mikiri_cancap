// Create deterministic message ID to prevent duplicates on retry
function createMessageId(role, content) {
    const timestamp = Date.now();
    const str = `${role}-${content}-${timestamp}`;
    // Simple hash function
    let hash = 0;
    for (let i = 0; i < str.length; i++) {
        const char = str.charCodeAt(i);
        hash = ((hash << 5) - hash) + char;
        hash = hash & hash; // Convert to 32bit integer
    }
    return `msg_${Math.abs(hash).toString(36)}_${timestamp}`;
}

async function saveMessageToFirestore(jobId, role, content, groundingMetadata = null) {
    try {
        // Create deterministic ID - same message always gets same ID
        const messageId = createMessageId(role, content);

        // Fetch job to get created_at timestamp for expire_at calculation
        const jobDoc = await db.collection('jobs').doc(jobId).get();
        const jobData = jobDoc.data();
        const jobCreatedAt = jobData?.created_at;

        // Calculate expire_at: job creation + 7 days (aligned with job retention policy)
        let expireAt;
        if (jobCreatedAt) {
            // Firestore Timestamp - add 7 days in milliseconds
            const expireMillis = jobCreatedAt.toMillis() + (7 * 24 * 60 * 60 * 1000);
            expireAt = firebase.firestore.Timestamp.fromMillis(expireMillis);
        } else {
            // Fallback: current time + 7 days (shouldn't happen, but be safe)
            expireAt = firebase.firestore.Timestamp.fromDate(
                new Date(Date.now() + 7 * 24 * 60 * 60 * 1000)
            );
        }

        const messageRef = db.collection('jobs').doc(jobId)
            .collection('chat_messages')
            .doc(messageId); // Use deterministic ID

        const messageData = {
            role: role,
            content: content,
            timestamp: firebase.firestore.FieldValue.serverTimestamp(),
            created_at: firebase.firestore.FieldValue.serverTimestamp(),
            expire_at: expireAt  // TTL field for 7-day retention
        };

        if (groundingMetadata) {
            messageData.grounding_metadata = groundingMetadata;
        }

        // Use merge: true to prevent overwrite on retry (idempotent)
        await messageRef.set(messageData, { merge: true });
        console.log(`Saved ${role} message to Firestore`);
        return { success: true };
    } catch (error) {
        console.error("Error saving message to Firestore:", error);
        return { success: false, error };
    }
}

// Simple recursive retry with exponential backoff
async function saveMessageWithRetry(jobId, role, content, groundingMetadata = null, retryCount = 0) {
    const result = await saveMessageToFirestore(jobId, role, content, groundingMetadata);

    if (result.success) {
        return result;
    }

    // If save failed, retry with exponential backoff
    if (retryCount >= 3) {
        console.error("Failed to save message after 3 retries");
        // Show subtle UI indicator (optional)
        return result;
    }

    // Exponential backoff: 100ms, 200ms, 400ms
    const delay = 100 * Math.pow(2, retryCount);
    await new Promise(resolve => setTimeout(resolve, delay));

    return saveMessageWithRetry(jobId, role, content, groundingMetadata, retryCount + 1);
}

async function loadChatHistory(jobId) {
    try {
        const user = firebase.auth().currentUser;
        console.log("[loadChatHistory] Current user UID:", user?.uid);

        // Check if job exists first (optional but good practice)
        const jobDoc = await db.collection('jobs').doc(jobId).get();
        if (!jobDoc.exists) {
            console.warn("[loadChatHistory] Job does not exist yet");
            return [];
        }

        const jobData = jobDoc.data();
        console.log("[loadChatHistory] Job user_id:", jobData?.user_id);
        console.log("[loadChatHistory] Current auth UID:", user?.uid);
        console.log("[loadChatHistory] UIDs match:", jobData?.user_id === user?.uid);

        const messagesRef = db.collection('jobs').doc(jobId)
            .collection('chat_messages')
            .orderBy('timestamp', 'asc'); // Order by server timestamp

        const snapshot = await messagesRef.get();
        console.log("[loadChatHistory] Found", snapshot.size, "messages");

        if (snapshot.empty) {
            return []; // No existing chat history
        }

        // If history exists, markdown was already sent in first message
        markdownFetched = true;

        const history = [];
        snapshot.forEach(doc => {
            const data = doc.data();
            history.push({
                role: data.role,
                content: data.content
            });

            // Render in UI
            addMessage(data.role, data.content, data.grounding_metadata || null);
        });

        return history;
    } catch (error) {
        console.error("[loadChatHistory] Error loading chat history:", error);
        console.error("[loadChatHistory] Error details:", error.message, error.code);
        // Continue without history - don't block user
        return [];
    }
}

