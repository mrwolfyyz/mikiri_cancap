// ===========================
function addMessage(role, content, groundingMetadata = null) {
    const messageDiv = document.createElement("div");
    messageDiv.className = `message ${role}`;

    const contentDiv = document.createElement("div");
    contentDiv.className = "message-content";

    // Render markdown for assistant messages, plain text for user
    if (role === "assistant") {
        contentDiv.innerHTML = renderMarkdown(content);
    } else {
        contentDiv.textContent = content;
    }
    messageDiv.appendChild(contentDiv);

    // Add search suggestions if present
    if (groundingMetadata && groundingMetadata.search_entry_point) {
        const suggestionsDiv = document.createElement("div");
        suggestionsDiv.className = "search-suggestions";

        // Create collapsible header
        const toggleDiv = document.createElement("div");
        toggleDiv.className = "search-suggestions-toggle";

        const title = document.createElement("h4");
        title.textContent = "Search Suggestions";

        const toggleIcon = document.createElement("span");
        toggleIcon.className = "toggle-icon";
        toggleIcon.textContent = "▼";

        toggleDiv.appendChild(title);
        toggleDiv.appendChild(toggleIcon);
        suggestionsDiv.appendChild(toggleDiv);

        // Toggle collapse on click
        toggleDiv.addEventListener("click", () => {
            suggestionsDiv.classList.toggle("collapsed");
        });

        const entryPoint = document.createElement("div");
        entryPoint.className = "search-entry-point";

        // Scope Google's styles to prevent affecting our page
        let htmlContent = groundingMetadata.search_entry_point;

        // Extract and scope the <style> tag
        const styleMatch = htmlContent.match(/<style>([\s\S]*?)<\/style>/);
        if (styleMatch) {
            const styleContent = styleMatch[1];
            // Scope all selectors to .search-entry-point
            const scopedStyle = styleContent
                .replace(/(@media[^{]*\{)/g, '.search-entry-point $1')
                .replace(/(^|\n)(\.[a-zA-Z][^{]*\{)/gm, '$1.search-entry-point $2')
                .replace(/(^|\n)([a-zA-Z][^{]*\{)/gm, '$1.search-entry-point $2');

            // Remove original style tag and add scoped one
            htmlContent = htmlContent.replace(/<style>[\s\S]*?<\/style>/, '');
            const scopedStyleTag = `<style>${scopedStyle}</style>`;
            htmlContent = scopedStyleTag + htmlContent;
        }

        // Sanitize HTML before rendering (compliance: preserve Google's styling and links)
        entryPoint.innerHTML = typeof DOMPurify !== "undefined"
            ? DOMPurify.sanitize(htmlContent, {
                ALLOWED_TAGS: ['style', 'div', 'span', 'a', 'img', 'br', 'p'],
                ALLOWED_ATTR: ['class', 'href', 'target', 'rel', 'src', 'alt', 'style']
            })
            : "";

        suggestionsDiv.appendChild(entryPoint);

        messageDiv.appendChild(suggestionsDiv);
    }

    elements.chatMessages.appendChild(messageDiv);

    // Scroll behavior: for assistant responses, scroll to show the start of the response
    // For user messages, scroll to bottom as usual
    if (role === "assistant") {
        // Scroll so the new message's top is at the top of the visible area
        messageDiv.scrollIntoView({ behavior: "smooth", block: "start" });
    } else {
        elements.chatMessages.scrollTop = elements.chatMessages.scrollHeight;
    }
}

function showError(message) {
    elements.errorContainer.textContent = message;
    elements.errorContainer.className = "error-message";
    elements.errorContainer.style.display = "block";
}

function hideError() {
    elements.errorContainer.style.display = "none";
}

function showTypingIndicator() {
    // Remove existing typing indicator if any
    hideTypingIndicator();

    const typingDiv = document.createElement("div");
    typingDiv.className = "typing-indicator";
    typingDiv.id = "typingIndicator";
    typingDiv.innerHTML = `
        <div class="dots">
            <span class="dot"></span>
            <span class="dot"></span>
            <span class="dot"></span>
        </div>
        <span>Searching and thinking...</span>
    `;
    elements.chatMessages.appendChild(typingDiv);
    elements.chatMessages.scrollTop = elements.chatMessages.scrollHeight;
}

function hideTypingIndicator() {
    const existing = document.getElementById("typingIndicator");
    if (existing) {
        existing.remove();
    }
}

function setLoading(loading) {
    elements.sendButton.disabled = loading;
    if (loading) {
        elements.sendButton.innerHTML = '<span class="loading"></span>';
        showTypingIndicator();
    } else {
        elements.sendButton.textContent = "Send";
        hideTypingIndicator();
    }
    elements.messageInput.disabled = loading;
}

// ===========================
// Message Handling
// ===========================
async function handleSendMessage() {
    const message = elements.messageInput.value.trim();
    if (!message || elements.sendButton.disabled) {
        return;
    }

    // Clear input
    elements.messageInput.value = "";
    hideError();

    // Add user message to UI
    addMessage("user", message);

    // Update conversation history
    conversationHistory.push({ role: "user", content: message });

    // Save user message to Firestore (with retry)
    if (jobId && db) {
        saveMessageWithRetry(jobId, "user", message).catch(err => {
            console.error("Failed to save user message to Firestore:", err);
            // Continue even if save fails
        });
    }

    setLoading(true);

    try {
        // Fetch markdown on first message if not already fetched
        // Only fetch if no history exists (history loading sets markdownFetched = true)
        if (conversationHistory.length === 1 && !markdownFetched && jobId) {
            try {
                markdownContext = await getMarkdown(jobId);
                markdownFetched = true;
                console.log("Fetched markdown context");
            } catch (error) {
                console.warn("Could not fetch markdown context:", error);
                // Continue without markdown context
            }
        }

        // Build request payload
        const payload = {
            message: message,
            conversation_history: conversationHistory.slice(0, -1), // Exclude current message
        };

        // Include markdown context only for first message
        if (markdownContext && conversationHistory.length === 1) {
            payload.markdown_context = markdownContext;
        }

        // Send to chat handler
        const response = await sendChatMessage(payload);

        // Update conversation history (cap to prevent unbounded growth)
        conversationHistory = response.conversation_history || conversationHistory;
        if (conversationHistory.length > MAX_HISTORY_LENGTH) {
            conversationHistory = conversationHistory.slice(-MAX_HISTORY_LENGTH);
        }

        // Add assistant response to UI
        addMessage(
            "assistant",
            response.response,
            response.grounding_metadata || null
        );

        // Save assistant message to Firestore (with retry)
        if (jobId && db) {
            saveMessageWithRetry(jobId, "assistant", response.response, response.grounding_metadata || null).catch(err => {
                console.error("Failed to save assistant message to Firestore:", err);
                // Continue even if save fails
            });
        }
    } catch (error) {
        console.error("Error handling message:", error);
        // Provide user-friendly error message
        let friendlyError = error.message;
        if (error.message.includes("timeout") || error.message.includes("Timeout")) {
            friendlyError = "The request took too long. This can happen with complex questions. Please try a shorter question or try again.";
        } else if (error.message.includes("network") || error.message.includes("fetch")) {
            friendlyError = "Unable to connect. Please check your internet connection and try again.";
        }
        showError(friendlyError);
        // Remove failed user message from history
        conversationHistory.pop();
    } finally {
        setLoading(false);
    }
}

// ===========================
// Event Listeners
// ===========================
function initEventListeners() {
    elements.sendButton.addEventListener("click", handleSendMessage);
    elements.messageInput.addEventListener("keydown", (e) => {
        if (e.key === "Enter" && !e.shiftKey) {
            e.preventDefault();
            handleSendMessage();
        }
    });
}

// ===========================
// Initialization
// ===========================
async function init() {
    // Load configuration first (from platform.json + firebase-config.json)
    const configLoaded = await loadConfig();
    if (!configLoaded) {
        console.error("Failed to load configuration. Application cannot start.");
        return;
    }

    // Initialize Firebase authentication and Firestore
    db = await initializeAuth();
    initEventListeners();

    // Get job ID from URL
    jobId = getJobIdFromURL();
    if (!jobId) {
        showError("No job ID provided. Please access this page from a completed investigation.");
        elements.messageInput.disabled = true;
        elements.sendButton.disabled = true;
        return;
    }

    elements.jobIdDisplay.textContent = jobId;

    // Load existing chat history
    if (db) {
        // Store initial welcome message before loading history
        const initialMessage = elements.chatMessages.querySelector('.message.assistant');
        const initialMessageHTML = initialMessage ? initialMessage.outerHTML : null;

        // Show loading indicator
        elements.chatMessages.innerHTML = '<div class="loading-history" style="padding: var(--space-md); text-align: center; color: var(--text-secondary);">Loading chat history...</div>';

        const history = await loadChatHistory(jobId);
        conversationHistory = history;

        // Remove loading indicator
        const loader = document.querySelector('.loading-history');
        if (loader) {
            loader.remove();
        }

        // If no history was loaded, restore the initial welcome message
        if (history.length === 0 && initialMessageHTML) {
            elements.chatMessages.innerHTML = initialMessageHTML;
        }
    }
}

// Start the app when DOM is ready
if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
} else {
    init();
}
